        public void cerrarTabs(){
        JTabbedPane padre=(JTabbedPane)this.getParent();
         if(padre!=null){
            if(((JTabbedPane)this.getParent()).getTabCount()>2){
                int aux=((JTabbedPane)this.getParent()).getTabCount();
                    for(int i=2; i<aux;i++){
                    ((JTabbedPane)this.getParent()).removeTabAt(2);
                    }  
                }  
            }
    }

